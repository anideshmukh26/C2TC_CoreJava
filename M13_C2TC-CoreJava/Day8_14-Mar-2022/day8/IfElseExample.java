// program of If else statement

package day8;

public class IfElseExample 
{
	public static void main(String[] args) 
	{
		int num= 10;
		if(num%2==0) //condition
		{
			System.out.println(num+  " is Even Number.");
		} else
		{
			System.out.println(num+ " is Odd Number.");
		}
	}
}

